// '=' Assigns a value to a variable, like in Python.

// '==' Compares to values and returns True if equal.

/* '===' 'Strict equality operator', compares values and
data types:    */
console.log(10===10)
console.log(10==='10')

// The same happens with INEQUALITY operators, but in reverse.
console.log(10!==10)
console.log(10!=='10')

// Other comparison operators (such as <=) work as in Python.

/* '&&' - 'and' - To check if a value is equal to 2 or more values,
in stead of using the keyword 'and', JS uses double '&&' symbol: */
if (30 <= 50 && 30 >= 25) {
    console.log('30 is between 25 and 50.')
}

